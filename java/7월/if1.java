class if1 {
    public static void main(String args[]){
        int num=73;
        if (num>=90){
            System.out.println("A");    
        }else if (num>=80){
            System.out.println("B");
        }else if (num>=70){
            System.out.println("C");
        }else if (num>=60){
            System.out.println("D");
        }else{
            System.out.println("F");
        }
        System.out.println("Done.");

    }
    
}
